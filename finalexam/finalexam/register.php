<?php
session_start();
session_destroy(); // Destroy existing session
session_start(); // Start a new session

require_once 'config/database.php';
require_once 'utils/email.php';

if ($_SERVER['REQUEST_METHOD'] == 'POST') {
    $username = trim($_POST['username']);
    $email = trim($_POST['email']);
    $password = $_POST['password'];
    $confirm_password = $_POST['confirm_password'];

    $errors = [];

    // Validation
    if (empty($username)) {
        $errors[] = "Username is required";
    }
    if (empty($email)) {
        $errors[] = "Email is required";
    } elseif (!filter_var($email, FILTER_VALIDATE_EMAIL)) {
        $errors[] = "Invalid email format";
    }
    if (empty($password)) {
        $errors[] = "Password is required";
    } elseif (strlen($password) < 6) {
        $errors[] = "Password must be at least 6 characters";
    }
    if ($password !== $confirm_password) {
        $errors[] = "Passwords do not match";
    }

    if (empty($errors)) {
        // Check if email already exists
        $stmt = $pdo->prepare("SELECT id FROM users WHERE email = ?");
        $stmt->execute([$email]);
        if ($stmt->rowCount() > 0) {
            $errors[] = "Email already registered";
        } else {
            // Create user
            $hashed_password = password_hash($password, PASSWORD_DEFAULT);
            $stmt = $pdo->prepare("INSERT INTO users (username, email, password) VALUES (?, ?, ?)");
            $stmt->execute([$username, $email, $hashed_password]);
            
            $user_id = $pdo->lastInsertId();
            
            // Generate OTP
            $otp = sprintf("%06d", mt_rand(1, 999999));
            $expires_at = date('Y-m-d H:i:s', strtotime('+5 minutes'));
            
            // Debug information
            error_log("Generated OTP: " . $otp . " for user: " . $user_id);
            error_log("Expires at: " . $expires_at);
            
            $stmt = $pdo->prepare("INSERT INTO otp (user_id, otp_code, expires_at) VALUES (?, ?, ?)");
            $stmt->execute([$user_id, $otp, $expires_at]);
            
            // Send OTP email
            if (sendOTPEmail($email, $otp)) {
                $_SESSION['user_id'] = $user_id;
                header("Location: verify_otp.php");
                exit();
            } else {
                $errors[] = "Failed to send OTP email";
            }
        }
    }
}
